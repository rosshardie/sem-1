package com.napier.sem;

/**
 * Represents a city
 */
public class City {
    /**
     * City ID
     */
    public int ID;

    /**
     * City Name
     */
    public String Name;

    /**
     * City CountryCode
     */
    public String CountryCode;

    /**
     * City District
     */
    public String District;

    /**
     * City Population
     */
    public int Population;
}